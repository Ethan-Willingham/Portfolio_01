#!/usr/bin/env node
/* ============================================================================
   build-site-size.mjs  -  bake the About-page "Size on disk" data
   (js/site-size-data.js): the total weight of the deployed site today, a
   breakdown by kind, and a day-by-day growth curve.

   Run from the repo root:
     node tools/build-site-size.mjs            # preview the numbers, write nothing
     node tools/build-site-size.mjs --write    # write js/site-size-data.js

   HOW: for each day in the reachable git history it takes that day's last
   commit and sums the blob sizes from `git ls-tree -r -l <sha>` (no checkout),
   counting only files the site actually serves (everything except the dev-only
   tools/, docs/, research/ and the root .md notes). The public history was
   truncated at the 2026-05-29 rewrite (see the commit-river notes), so the curve
   starts there; that is the whole stretch where the posts were written anyway.
   ============================================================================ */
import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { execSync } from 'node:child_process';

const REPO = process.cwd();
const WRITE = process.argv.includes('--write');
const OUT = join(REPO, 'js/site-size-data.js');

// a file counts toward "the site" unless it is dev-only scaffolding
function deployed(path) {
  if (/^(tools|docs|research)\//.test(path)) return false;   // build scripts, design notes, dossiers
  if (/^[^/]*\.md$/.test(path)) return false;                // root notes (AGENTS, CLAUDE, STYLE, ...)
  if (/^\.[^/]*$/.test(path)) return false;                  // dotfiles at root (.gitignore, ...)
  return true;
}

// kind of a file, for the current-day breakdown
function kindOf(path) {
  const e = (path.toLowerCase().match(/\.([a-z0-9]+)"?$/) || [, ''])[1];
  if (/^(jpg|jpeg|png|webp|gif|svg|ico|avif)$/.test(e)) return 'Images';
  if (/^(m4a|mp3|wav|ogg|aac)$/.test(e)) return 'Audio';
  if (/^(woff2?|ttf|otf)$/.test(e)) return 'Fonts';
  if (/^(glb|gltf|bin|mz3|ply)$/.test(e)) return '3D & data';
  if (/^(html|css|js|json|txt|xml|map|webmanifest)$/.test(e)) return 'Code & text';
  return 'Other';
}

// sum deployed blob sizes (and per-kind sizes) for one commit, with no checkout
function measure(sha) {
  const out = execSync(`git ls-tree -r -l ${sha}`, { encoding: 'utf8', maxBuffer: 1 << 28 });
  let total = 0; const byKind = {};
  for (const line of out.split('\n')) {
    const tab = line.indexOf('\t');
    if (tab < 0) continue;
    const size = +line.slice(0, tab).trim().split(/\s+/)[3];
    const path = line.slice(tab + 1);
    if (!size || !deployed(path)) continue;
    total += size;
    const k = kindOf(path); byKind[k] = (byKind[k] || 0) + size;
  }
  return { total, byKind };
}

// one representative commit (the day's last) per calendar day, oldest first
const log = execSync(`git log --format='%H|%ct|%cd' --date=format:'%Y-%m-%d'`, { encoding: 'utf8', maxBuffer: 1 << 28 });
const lastPerDay = new Map();        // day -> {sha, ts}  (overwrite keeps the earliest seen = newest, since log is newest-first)
for (const line of log.split('\n')) {
  if (!line.trim()) continue;
  const [sha, ct, day] = line.split('|');
  if (!lastPerDay.has(day)) lastPerDay.set(day, { sha, ts: +ct });   // first hit per day = that day's newest commit
}
const days = [...lastPerDay.entries()].sort((a, b) => a[1].ts - b[1].ts);  // oldest -> newest

const fmt = n => n >= 1 << 30 ? (n / (1 << 30)).toFixed(2) + ' GB'
  : n >= 1 << 20 ? (n / (1 << 20)).toFixed(1) + ' MB'
  : n >= 1 << 10 ? (n / (1 << 10)).toFixed(0) + ' KB' : n + ' B';

const series = [];
for (const [day, { sha, ts }] of days) series.push({ day, ts, sha, total: measure(sha).total });
const latest = measure(days[days.length - 1][1].sha);
const cats = Object.entries(latest.byKind).map(([name, bytes]) => ({ name, bytes })).sort((a, b) => b.bytes - a.bytes);

const data = {
  generated: Math.floor(Date.now() / 1000),
  note: 'Total bytes of every file the site serves (all but tools/, docs/, research/ and root notes), measured from git blob sizes. Series is one sample per day since the 2026-05-29 history rewrite. Rebuilt by tools/build-site-size.mjs.',
  totalBytes: latest.total,
  categories: cats,
  series: series.map(s => [s.ts, s.total]),
};

console.log(`days sampled: ${series.length}  (${series[0].day} -> ${series[series.length - 1].day})`);
console.log(`first: ${fmt(series[0].total)}   today: ${fmt(data.totalBytes)}   (${(data.totalBytes / series[0].total).toFixed(1)}x)`);
console.log('by kind today:'); for (const c of cats) console.log(`  ${fmt(c.bytes).padStart(9)}  ${c.name}`);

if (WRITE) {
  const banner = '/* Auto-generated by tools/build-site-size.mjs. The deployed-size growth curve\n' +
    '   on the About page. Do not hand-edit; re-run the script. */\n';
  writeFileSync(OUT, banner + 'window.SITE_SIZE = ' + JSON.stringify(data) + ';\n');
  console.log('\nWROTE js/site-size-data.js');
} else {
  console.log('\n(dry run) re-run with --write to save js/site-size-data.js');
}
