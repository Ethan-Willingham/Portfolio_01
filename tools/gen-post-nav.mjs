/* gen-post-nav.mjs  -  stamp the endcap nav into every collection member post.
   Every hub member ends with a quiet .u-next block (next post in the collection,
   wrapping at the end, plus the collection itself and Home), and its top back
   link points at the collection it lives in instead of Home, so a reader who
   finishes a post is never left at a dead end and never loses their place in
   the tree. The single shelf post gets a shelf-flavoured endcap.

   Data comes from tools/gen-hubs.mjs (the one source of truth for collection
   membership and order). Idempotent: re-run any time; existing endcaps are
   replaced in place. Run from the repo root:  node tools/gen-post-nav.mjs
   The block is injected AFTER </main> (before the site footer) on purpose:
   build-search-index.mjs indexes <main> only, and it also strips .u-next
   defensively, so endcap titles never pollute search results. No em dashes. */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { HUBS, SHELF, ordered } from './gen-hubs.mjs';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const esc = (s) => String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');

const MARK_OPEN = '<!-- endcap nav, generated by tools/gen-post-nav.mjs; the data lives in tools/gen-hubs.mjs -->';

function endcapHTML(kicker, nextHref, nextTitle, moreLinks) {
  const more = moreLinks
    .map((l) => `<a href="${l.href}">${esc(l.label)}</a>`)
    .join('<span class="u-next-sep">&middot;</span>');
  return `  ${MARK_OPEN}
  <nav class="u-next" aria-label="Keep reading">
    <p class="u-next-k">${esc(kicker)}</p>
    <p class="u-next-t"><a class="u-next-link" href="${nextHref}">${esc(nextTitle)}</a></p>
    <p class="u-next-more">${more}</p>
  </nav>
`;
}

function stamp(file, endcap, backHref, backLabel) {
  const path = join(ROOT, file);
  if (!existsSync(path)) return `MISSING ${file}`;
  let html = readFileSync(path, 'utf8');

  // 1) replace or insert the endcap block, just above the site footer
  html = html.replace(/[ \t]*<!-- endcap nav,[\s\S]*?<\/nav>\n/, '');
  const foot = html.indexOf('<footer class="site-footer"');
  if (foot < 0) return `NO-FOOTER ${file}`;
  const lineStart = html.lastIndexOf('\n', foot) + 1;
  html = html.slice(0, lineStart) + endcap + html.slice(lineStart);

  // 2) point the top back link at the collection instead of Home (a no-op
  // rewrite on an already-pointed link is fine; only a MISSING link is worth
  // reporting)
  const hasBack = /<a[^>]*class="post-back/.test(html);
  html = html
    .replace(/(<a\s+href=")[^"]*("\s+class="post-back[^"]*")/g, `$1${backHref}$2`)
    .replace(/(<a\s+class="post-back[^"]*"\s+href=")[^"]*(")/g, `$1${backHref}$2`)
    .replace(/(<a[^>]*class="post-back[^"]*"[^>]*>\s*<span class="pb-label">)[^<]*(<\/span>)/g, `$1${esc(backLabel)}$2`);

  writeFileSync(path, html);
  return `ok ${file}${hasBack ? '' : ' (no post-back link found)'}`;
}

const report = [];
for (const h of HUBS) {
  const members = ordered(h.members).filter((m) => !m.soon);
  const hubHref = `${h.slug}.html`;
  members.forEach((m, i) => {
    const next = members[(i + 1) % members.length];
    const endcap = endcapHTML(
      `Next in ${h.title}`,
      next.href, next.title,
      [
        { href: hubHref, label: `All ${members.length} posts in ${h.title}` },
        { href: '/', label: 'Home' },
      ],
    );
    report.push(stamp(m.href, endcap, hubHref, h.title));
  });
}

for (const p of SHELF.singles) {
  const endcap = endcapHTML(
    'More on the shelf',
    `${SHELF.path}.html`, SHELF.title,
    [{ href: '/', label: 'Home' }],
  );
  report.push(stamp(p.href, endcap, `${SHELF.path}.html`, SHELF.title));
}

const bad = report.filter((r) => !r.startsWith('ok'));
console.log(`gen-post-nav: stamped ${report.length - bad.length}/${report.length} posts`);
for (const r of report) if (!r.startsWith('ok') || r.includes('(')) console.log('  ' + r);
